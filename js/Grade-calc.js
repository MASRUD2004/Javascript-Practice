const subjectNum=33;
const fullNum=100;
const grade=(subjectNum/fullNum*100);

if(grade>=80){
    console.log("You have got A+");
}
else if(grade>=75){
    console.log("You have got A");
}
else if(grade>=70){
    console.log("You have got A-");
}
else if(grade>=60){
    console.log("You have got B");
}
else if(grade>=50){
    console.log("You have got c");
}
else if(grade>=40){
    console.log("You have got D");
}
else if(grade>=34){
    console.log("You have got E");
}
else{
    console.log("you have failed");
}
    

